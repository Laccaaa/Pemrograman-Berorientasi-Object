package pertemuansebelas;

public class MataKuliah {
    private String kodeMk;
    private String sks;
    private String namaMk;
    private String semesterAjar;

    /**
     * @return the kodeMk
     */
    public String getKodeMk() {
        return kodeMk;
    }

    /**
     * @param kodeMk the kodeMk to set
     */
    public void setKodeMk(String kodeMk) {
        this.kodeMk = kodeMk;
    }

    /**
     * @return the sks
     */
    public String getSks() {
        return sks;
    }

    /**
     * @param sks the sks to set
     */
    public void setSks(String sks) {
        this.sks = sks;
    }

    /**
     * @return the namaMk
     */
    public String getNamaMk() {
        return namaMk;
    }

    /**
     * @param namaMk the namaMk to set
     */
    public void setNamaMk(String namaMk) {
        this.namaMk = namaMk;
    }

    /**
     * @return the semesterAjar
     */
    public String getSemesterAjar() {
        return semesterAjar;
    }

    /**
     * @param semesterAjar the semesterAjar to set
     */
    public void setSemesterAjar(String semesterAjar) {
        this.semesterAjar = semesterAjar;
    }
}
