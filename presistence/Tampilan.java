    private void tampil() {
        EntityManager em = Persistence.createEntityManagerFactory("PresistencePU").createEntityManager();

        Query q = em.createNamedQuery("Matakuliah.findAll");
        List<Matakuliah> hasil = q.getResultList();

        tbmk = new DefaultTableModel(new String[]{"KodeMk", "sks", "NamaMk", "SemesterAjar"}, 0);
        for (Matakuliah data : hasil) {
            Object[] baris = new Object[5];
            baris[0] = data.getKodemk();
            baris[1] = data.getSks();
            baris[2] = data.getNamamk();
            baris[3] = data.getSemesterajar();
            tbmk.addRow(baris);
        }
        Tabel.setModel(tbmk);
    }

