    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:  
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PresistencePU");
        EntityManager em = emf.createEntityManager();

        try {
            int sksValue;
            try {
                sksValue = Integer.parseInt(txtSks.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "SKS harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            em.getTransaction().begin();
            Matakuliah matakuliah = em.find(Matakuliah.class, txtKode.getText());

            if (matakuliah != null) {
                matakuliah.setSks(sksValue);
                matakuliah.setNamamk(txtNama.getText());
                matakuliah.setSemesterajar(Integer.parseInt(txtSmt.getText()));

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "Gagal memperbarui data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            em.close();
            emf.close();
        }
        clear();
        tampil();
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PresistencePU");
        EntityManager em = emf.createEntityManager();

        try {
            em.getTransaction().begin();

            Matakuliah matakuliah = new Matakuliah();
            matakuliah.setKodemk(txtKode.getText());
            matakuliah.setSks(Integer.parseInt(txtSks.getText()));
            matakuliah.setNamamk(txtNama.getText());
            matakuliah.setSemesterajar(Integer.parseInt(txtSmt.getText()));

            em.persist(matakuliah);

            em.getTransaction().commit();
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception e) {

            em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "Gagal menambahkan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            em.close();
            emf.close();
        }
        clear();
        tampil();
    }//GEN-LAST:event_btnTambahActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PresistencePU");
        EntityManager em = emf.createEntityManager();

        try {
            em.getTransaction().begin();
            Matakuliah matakuliah = em.find(Matakuliah.class, txtKode.getText());

            if (matakuliah != null) {
                // Remove the entity
                em.remove(matakuliah);

                em.getTransaction().commit();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Data tidak ditemukan!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            em.getTransaction().rollback();
            JOptionPane.showMessageDialog(this, "Gagal menghapus data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        } finally {
            em.close();
            emf.close();
        }
        clear();
        tampil();
    }//GEN-LAST:event_btnHapusActionPerformed

